package by.incubator.objectfactory;

public class Apple {
    private double weight;
    private String color;

    public Apple() {
        weight = 0;
        color = "red";
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(Double weight) {
        this.weight = weight;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
